/// Sparsity: at l1_ratio=0.9, alpha=1.0 the exactly-zero support set must match
/// sklearn. sklearn zeros features {1,3,4,6,7,8,9}, keeps {0,2,5}.
#[test]
fn elastic_net_support_set_parity() {
    let fitted = fit_converged(1.0, 0.9, true);
    let coef = fitted.coefficients();
    for &j in &[1usize, 3, 4, 6, 7, 8, 9] {
        assert!(
            coef[j] == 0.0,
            "feature {j} should be exactly zero (sklearn zeros it), got {}",
            coef[j]
        );
    }
    for &j in &[0usize, 2, 5] {
        assert!(
            coef[j] != 0.0,
            "feature {j} should be nonzero (sklearn keeps it), got 0.0"
        );
    }
}

/// L2-only-doesn't-zero: at low l1_ratio (0.1) the L2 term keeps more
/// coefficients non-zero than the Lasso (l1_ratio=1) limit. Sanity vs oracle:
/// sklearn EN(alpha=1.0, l1_ratio=0.1) keeps 9 of 10 features non-zero, whereas
/// the l1_ratio=0.9 fit keeps only 3.
#[test]
fn elastic_net_low_l1_ratio_keeps_coeffs() {
    let low = fit_converged(1.0, 0.1, true);
    let high = fit_converged(1.0, 0.9, true);
    let nz = |c: &Array1<f64>| c.iter().filter(|&&v| v != 0.0).count();
    assert!(
        nz(low.coefficients()) > nz(high.coefficients()),
        "low l1_ratio should keep more nonzero coeffs: low={} high={}",
        nz(low.coefficients()),
        nz(high.coefficients())
    );
    // sklearn oracle: EN(1.0,0.1) keeps 9 non-zero.
    assert_eq!(
        nz(low.coefficients()),
        9,
        "sklearn EN(alpha=1.0,l1_ratio=0.1) keeps 9 non-zero coeffs"
    );
}

