/// Stress: a badly-scaled feature column (X[:,0] *= 100) stresses the per-column
/// L2 denominator (col_norm + alpha*(1-l1_ratio)). Converged ferrolearn must
/// match sklearn ElasticNet(alpha=0.1, l1_ratio=0.5) on the scaled design.
/// Oracle (max_iter=2e6, tol=1e-15) converged. A denominator/L2-split bug would
/// surface here even though the well-scaled grid passes.
#[test]
fn elastic_net_scaled_feature_parity_converged() {
    let mut x = design();
    for i in 0..50 {
        x[[i, 0]] *= 100.0;
    }
    let sk_coef = [
        0.03020117373208762,
        0.0,
        -1.8113936514773306,
        0.0,
        -0.0017567566151168606,
        1.3650218077609573,
        0.0,
        0.0,
        0.0,
        0.40599492146655086,
    ];
    let sk_int = 0.021751120773870758;

    let fitted = ElasticNet::<f64>::new()
        .with_alpha(0.1)
        .with_l1_ratio(0.5)
        .with_max_iter(2_000_000)
        .with_tol(1e-15)
        .fit(&x, &target())
        .unwrap();
    let coef = fitted.coefficients();
    for j in 0..10 {
        let diff = (coef[j] - sk_coef[j]).abs();
        assert!(
            diff < 1e-5,
            "scaled coef[{j}]: ferrolearn={} sklearn={} diff={diff}",
            coef[j],
            sk_coef[j]
        );
    }
    let idiff = (fitted.intercept() - sk_int).abs();
    assert!(idiff < 1e-5, "scaled intercept diff={idiff}");
}
