/// l1_ratio=1 -> Lasso equivalence. Converged ElasticNet(alpha=0.1, l1_ratio=1)
/// must equal sklearn ElasticNet(l1_ratio=1) AND ferrolearn Lasso(alpha=0.1).
/// Oracle: both sklearn estimators give identical coef/intercept (verified).
#[test]
fn elastic_net_l1_ratio_one_equals_lasso() {
    let sk_coef = [
        2.92211366838258,
        0.0,
        -1.8529025982441323,
        0.0,
        0.0,
        1.3885131080378175,
        0.0,
        0.0,
        0.0,
        0.36498288412756924,
    ];
    let sk_int = 0.006040004866284199;

    let en = fit_converged(0.1, 1.0, true);
    let coef = en.coefficients();
    for j in 0..10 {
        let diff = (coef[j] - sk_coef[j]).abs();
        assert!(
            diff < 1e-5,
            "EN(l1_ratio=1) coef[{j}]: ferrolearn={} sklearn={} diff={diff}",
            coef[j],
            sk_coef[j]
        );
    }
    assert!((en.intercept() - sk_int).abs() < 1e-5);

    // Cross-check: ferrolearn ElasticNet(l1_ratio=1) == ferrolearn Lasso(alpha).
    let la = Lasso::<f64>::new()
        .with_alpha(0.1)
        .with_max_iter(200_000)
        .with_tol(1e-14)
        .fit(&design(), &target())
        .unwrap();
    let lcoef = la.coefficients();
    for j in 0..10 {
        let diff = (coef[j] - lcoef[j]).abs();
        assert!(
            diff < 1e-6,
            "EN(l1_ratio=1) vs ferrolearn Lasso coef[{j}]: EN={} Lasso={} diff={diff}",
            coef[j],
            lcoef[j]
        );
    }
}

