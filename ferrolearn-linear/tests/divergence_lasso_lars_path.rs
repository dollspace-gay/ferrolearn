/// Divergence: `ferrolearn_linear::LassoLars::fit` diverges from
/// `sklearn/linear_model/_least_angle.py:413+` (`method == "lasso"`) on the
/// diabetes dataset at `alpha=0.1`. sklearn's equiangular LARS-Lasso homotopy
/// (with the section 3.3 drop condition) admits feature 9 and returns
/// `coef_ ~ [0, -155.34, 517.22, 275.09, -52.55, 0, -210.14, 0, 483.92, 33.66]`;
/// ferrolearn's forward-stepwise OLS (`fn ols_active`) admits feature 4 (not 9)
/// and returns `coef_ ~ [0, -233.09, 527.02, 315.45, 0, -110.93, -289.40, 0,
/// 479.23, 70.08]`. Tracking: #482.
#[test]
#[ignore = "divergence: LassoLars uses forward-stepwise OLS, not the equiangular LARS-Lasso drop path; tracking #482"]
fn divergence_lasso_lars_coef_alpha_0_1() {
    let (x, y) = diabetes();
    let fitted = LassoLars::<f64>::new().with_alpha(0.1).fit(&x, &y).unwrap();
    let coef = fitted.coefficients();

    // Active-set divergence: sklearn admits exactly SK_LASSO_A01_ACTIVE.
    let ferro_active: Vec<usize> = coef
        .iter()
        .enumerate()
        .filter(|&(_, &c)| c.abs() > 1e-9)
        .map(|(i, _)| i)
        .collect();
    assert_eq!(
        ferro_active,
        SK_LASSO_A01_ACTIVE.to_vec(),
        "active set diverges: ferrolearn {ferro_active:?} vs sklearn {SK_LASSO_A01_ACTIVE:?}"
    );

    // Coefficient divergence (LARS-Lasso is exact -> tight tolerance).
    for (j, &sk) in SK_LASSO_A01.iter().enumerate() {
        let got = coef[j];
        assert!(
            (got - sk).abs() <= 1e-4,
            "coef[{j}] diverges: ferrolearn {got} vs sklearn {sk}"
        );
    }
    assert!(
        (fitted.intercept() - SK_LASSO_A01_INTERCEPT).abs() <= 1e-4,
        "intercept diverges: ferrolearn {} vs sklearn {SK_LASSO_A01_INTERCEPT}",
        fitted.intercept()
    );
}

