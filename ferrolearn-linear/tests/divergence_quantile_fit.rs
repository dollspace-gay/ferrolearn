/// Divergence: under `alpha=1.0` sklearn drives all coefficients to EXACTLY
/// zero — the LP sparse vertex (`sklearn/linear_model/_quantile.py:238-248`,
/// L1 cost `alpha*1_p`); the intercept stays a free variable
/// (`intercept_ ≈ 0.7387` at `quantile=0.5`). ferrolearn's IRLS-linearized
/// ridge penalty (`scaled_alpha / max(|w_j|, eps)` in `weighted_l1_solve`)
/// only reaches NEAR-zero coefficients with an identical ≈ 0.912 centering
/// intercept, so it cannot reach the LP vertex.
/// Tracking: #332, #340.
#[test]
fn divergence_alpha1_sparse_vertex() {
    let (x, y) = oracle_dataset();
    let fitted = QuantileRegressor::<f64>::new()
        .with_quantile(0.5)
        .with_alpha(1.0)
        .with_max_iter(2000)
        .fit(&x, &y)
        .unwrap();
    let coef = fitted.coefficients();
    for (j, &sk) in SK_Q05_A1_COEF.iter().enumerate() {
        assert!(
            (coef[j] - sk).abs() < 1e-8,
            "q=0.5 alpha=1.0 coef[{j}] must be EXACTLY zero (LP vertex): \
             sklearn {sk}, ferrolearn {}",
            coef[j]
        );
    }
    let intercept = fitted.intercept();
    assert!(
        (intercept - SK_Q05_A1_INTERCEPT).abs() < 1e-6,
        "q=0.5 alpha=1.0 intercept: sklearn {SK_Q05_A1_INTERCEPT}, ferrolearn {intercept}"
    );
}

/// Divergence: under `alpha=1.0` the intercept is quantile-dependent in
/// sklearn (`0.7387` at q=0.5 vs `2.7406` at q=0.8,
/// `sklearn/linear_model/_quantile.py:304`); ferrolearn returns an identical
/// ≈ 0.912 intercept for both quantiles.
/// Tracking: #332, #506.
#[test]
fn divergence_alpha1_intercept_quantile_dependent() {
    let (x, y) = oracle_dataset();
    let f08 = QuantileRegressor::<f64>::new()
        .with_quantile(0.8)
        .with_alpha(1.0)
        .with_max_iter(2000)
        .fit(&x, &y)
        .unwrap();
    let intercept = f08.intercept();
    assert!(
        (intercept - SK_Q08_A1_INTERCEPT).abs() < 1e-6,
        "q=0.8 alpha=1.0 intercept: sklearn {SK_Q08_A1_INTERCEPT}, ferrolearn {intercept}"
    );
    // The L1 sparse vertex also holds at q=0.8: coef is exactly [0,0,0].
    let coef = f08.coefficients();
    for (j, &sk) in SK_Q08_A1_COEF.iter().enumerate() {
        assert!(
            (coef[j] - sk).abs() < 1e-8,
            "q=0.8 alpha=1.0 coef[{j}] must be EXACTLY zero (LP vertex): \
             sklearn {sk}, ferrolearn {}",
            coef[j]
        );
    }
}

