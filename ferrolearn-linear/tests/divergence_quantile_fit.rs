/// Characterization: at `quantile=0.5, alpha=0.0` (median regression, no
/// penalty) ferrolearn's IRLS intercept lands NEAR sklearn's LP intercept
/// (`sklearn/linear_model/_quantile.py:304`, ≈ 0.3243). This isolates the
/// divergence to (a) non-median quantiles and (b) nonzero alpha. This test is
/// EXPECTED TO PASS at the documented ~0.03 tolerance and is the control for
/// the failing pins above — it is NOT ignored.
#[test]
fn near_match_median_no_penalty() {
    let (x, y) = oracle_dataset();
    let fitted = QuantileRegressor::<f64>::new()
        .with_quantile(0.5)
        .with_alpha(0.0)
        .with_max_iter(2000)
        .fit(&x, &y)
        .unwrap();
    let intercept = fitted.intercept();
    // Loose tolerance: IRLS only approximately coincides with the LP here.
    assert!(
        (intercept - SK_Q05_A0_INTERCEPT).abs() < 0.05,
        "q=0.5 alpha=0.0 intercept near-match: sklearn {SK_Q05_A0_INTERCEPT}, \
         ferrolearn {intercept}"
    );
}
