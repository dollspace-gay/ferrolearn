/// `QuantileRegressor(quantile=0.8, alpha=1.0).fit(X,y).coef_` — exact sparse 0.
const SK_Q08_A1_COEF: [f64; 3] = [0.0, 0.0, 0.0];
/// `QuantileRegressor(quantile=0.5, alpha=1.0).fit(X,y).coef_` — exact sparse 0.
const SK_Q05_A1_COEF: [f64; 3] = [0.0, 0.0, 0.0];
/// `QuantileRegressor(quantile=0.5, alpha=1.0).fit(X,y).intercept_`.
const SK_Q05_A1_INTERCEPT: f64 = 0.7387077239744255;
/// `QuantileRegressor(quantile=0.8, alpha=1.0).fit(X,y).intercept_`.
const SK_Q08_A1_INTERCEPT: f64 = 2.740633561059503;

// ---------------------------------------------------------------------------
// PRIMARY pin (#340/#506): quantile-invariance / intercept divergence.
// ---------------------------------------------------------------------------

/// Divergence: ferrolearn's `QuantileRegressor::fit` diverges from
/// `sklearn/linear_model/_quantile.py:298-304` for `quantile=0.8, alpha=0.0`
/// on the doc-author dataset. sklearn's HiGHS LP returns `intercept_ ≈ 0.8815`
/// (the intercept is a free LP variable, `params[0] = s0 - t0`); ferrolearn's
/// centering-recovered intercept (`intercept = y_mean - x_mean·w`, `fn fit`)
/// returns ≈ 0.2988 — the SAME value as its `quantile=0.5` fit, because the
/// IRLS fit is quantile-invariant.
/// Tracking: #340, #506.
#[test]
fn divergence_quantile_08_intercept_diverges_from_lp() {
    let (x, y) = oracle_dataset();
    let fitted = QuantileRegressor::<f64>::new()
        .with_quantile(0.8)
        .with_alpha(0.0)
        .with_max_iter(2000)
        .fit(&x, &y)
        .unwrap();
    let intercept = fitted.intercept();
    assert!(
        (intercept - SK_Q08_A0_INTERCEPT).abs() < 1e-6,
        "q=0.8 alpha=0.0 intercept: sklearn {SK_Q08_A0_INTERCEPT}, ferrolearn {intercept}"
    );
}

/// Divergence: the fit is quantile-INVARIANT. sklearn's intercept changes by
/// ≈ 0.557 between `quantile=0.5` and `quantile=0.8`
/// (`sklearn/linear_model/_quantile.py:298-304`, the LP cost
/// `quantile*1_n / (1-quantile)*1_n` at `:241-242` shifts the optimum).
/// ferrolearn's centering intercept (`fn fit`) is identical across quantiles,
/// so the observed difference is ≈ 0 rather than ≈ 0.557.
/// Tracking: #506.
#[test]
fn divergence_quantile_intercept_must_change_with_quantile() {
    let (x, y) = oracle_dataset();
    let f05 = QuantileRegressor::<f64>::new()
        .with_quantile(0.5)
        .with_alpha(0.0)
        .with_max_iter(2000)
        .fit(&x, &y)
        .unwrap();
    let f08 = QuantileRegressor::<f64>::new()
        .with_quantile(0.8)
        .with_alpha(0.0)
        .with_max_iter(2000)
        .fit(&x, &y)
        .unwrap();
    let observed_diff = f08.intercept() - f05.intercept();
    assert!(
        (observed_diff - SK_INTERCEPT_DIFF_Q08_Q05).abs() < 1e-6,
        "intercept(q=0.8) - intercept(q=0.5): sklearn {SK_INTERCEPT_DIFF_Q08_Q05}, \
         ferrolearn {observed_diff} (≈0 — quantile-invariant fit)"
    );
}

/// Divergence: ferrolearn's `coef_` at `quantile=0.8, alpha=0.0` does not match
/// sklearn's LP vertex (`sklearn/linear_model/_quantile.py:303`); ferrolearn
/// returns its quantile=0.5 coefficients regardless of the quantile.
/// Tracking: #340.
#[test]
fn divergence_quantile_08_coef_diverges_from_lp() {
    let (x, y) = oracle_dataset();
    let fitted = QuantileRegressor::<f64>::new()
        .with_quantile(0.8)
        .with_alpha(0.0)
        .with_max_iter(2000)
        .fit(&x, &y)
        .unwrap();
    let coef = fitted.coefficients();
    for (j, &sk) in SK_Q08_A0_COEF.iter().enumerate() {
        assert!(
            (coef[j] - sk).abs() < 1e-6,
            "q=0.8 alpha=0.0 coef[{j}]: sklearn {sk}, ferrolearn {}",
            coef[j]
        );
    }
}

