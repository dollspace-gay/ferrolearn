//! Divergence pins for `QuantileRegressor::fit` against the live scikit-learn
//! 1.5.2 oracle (`sklearn/linear_model/_quantile.py`).
//!
//! sklearn builds the exact quantile-regression LINEAR PROGRAM and solves it
//! with `scipy.optimize.linprog` (HiGHS): decision vector
//! `x = (s0, s, t0, t, u, v) >= 0` with `intercept = s0 - t0`,
//! `coef = s - t`, residual `y - X@coef - intercept = u - v`, cost
//! `c = (0, alpha*1_p, 0, alpha*1_p, quantile*1_n, (1-quantile)*1_n)`,
//! `A_eq = (1_n, X, -1_n, -X, diag(1_n), -diag(1_n))`, `b_eq = y`
//! (`sklearn/linear_model/_quantile.py:218-269`). The intercept is therefore a
//! FREE LP VARIABLE and the fit depends on `quantile`:
//!   "Note that centering y and X with _preprocess_data does not work for
//!    quantile regression." (`sklearn/linear_model/_quantile.py:177`).
//!
//! ferrolearn now solves this exact LP with a self-contained two-phase primal
//! simplex (Bland's rule) in `fn fit` of `quantile_regressor.rs` (the intercept
//! is a free LP variable, not centering-recovered), reaching sklearn's HiGHS
//! vertex. Previously it fit via IRLS on a smoothed pinball loss and recovered
//! the intercept by centering, which made the fit quantile-INVARIANT and unable
//! to reach the LP's sparse vertex under regularization — the divergence these
//! tests pinned (now closed).
//!
//! Expected values are computed by the LIVE sklearn 1.5.2 oracle (R-CHAR-3),
//! NOT copied from the ferrolearn side. To regenerate:
//! ```text
//! python3 -c "
//! from sklearn.linear_model import QuantileRegressor
//! import numpy as np
//! rng=np.random.RandomState(0); X=rng.randn(30,3)
//! y=X@np.array([1.,2.,-1.])+0.5*rng.randn(30)
//! for q in (0.5,0.8):
//!   for a in (0.0,1.0):
//!     m=QuantileRegressor(quantile=q,alpha=a).fit(X,y)
//!     print(q,a,m.coef_.tolist(),m.intercept_)"
//! ```
//!
//! Tracking: #340 (IRLS vs HiGHS LP), #506 (centering intercept invalid for
//! quantile regression), #332 (alpha L1 sparse vertex).

