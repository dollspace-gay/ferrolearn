// --- chi2 extra df: 3-class -> df=2 (gamma-based SF) -----------------------

#[test]
#[allow(
    clippy::assertions_on_constants,
    reason = "error arm fails loudly without panic!/unwrap (anti-pattern gate)"
)]
fn reaudit_chi2_3class_df2_pvalues() {
    // scipy chdtrc(2, .) : feature0 p=0.19691167520419403, feature1 p=0.26359713811572677
    const SK_P0: f64 = 0.196_911_675_204_194_03;
    const SK_P1: f64 = 0.263_597_138_115_726_77;
    let x = array![
        [2.0_f64, 1.0],
        [0.0, 3.0],
        [1.0, 2.0],
        [3.0, 0.0],
        [2.0, 2.0],
        [0.0, 1.0]
    ];
    let y: Array1<usize> = array![0, 1, 2, 0, 1, 2];
    match chi2(&x, &y) {
        Ok((_c, p)) => {
            assert!(
                (p[0] - SK_P0).abs() < 1e-6,
                "chi2 df=2 p[0]: ferrolearn {} vs scipy {SK_P0}",
                p[0]
            );
            assert!(
                (p[1] - SK_P1).abs() < 1e-6,
                "chi2 df=2 p[1]: ferrolearn {} vs scipy {SK_P1}",
                p[1]
            );
        }
        Err(e) => assert!(false, "chi2 returned Err: {e}"),
    }
}
