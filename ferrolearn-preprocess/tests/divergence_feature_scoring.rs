// ===========================================================================
// BROAD F-DISTRIBUTION SF RE-AUDIT (post-fix #1416 / DIV-1 #1417).
//
// The rewritten `regularized_incomplete_beta` + `betacf` Lentz continued
// fraction (feature_scoring.rs:520-608) is the WHOLE F-distribution survival
// function for f_classif/f_regression. These probes exercise it across
// small-p (deep tail) / large-p / moderate-p and varied (df1, df2) regimes
// against the LIVE scipy/sklearn oracle (scipy 1.17.1 / sklearn 1.5.2).
//
// All expected p-values produced by:
//   from sklearn.feature_selection import f_classif, f_regression
//   import scipy.special as sp            # sp.fdtrc(df1,df2,F)
// and hard-coded full-precision per R-CHAR-3. The F-statistic itself is
// green-guarded above, so any p mismatch is purely the beta/SF path.
//
// abs tol 1e-6 for p >= 1e-4; relative tol 1e-6 for tiny tail p (< 1e-4).
// ===========================================================================

/// Assert ferrolearn p ~= scipy p: absolute 1e-6 normally, relative 1e-6 in
/// the deep tail where the absolute value is below 1e-4.
#[allow(
    clippy::assertions_on_constants,
    reason = "fails loudly without panic!/unwrap (anti-pattern gate)"
)]
fn assert_p_close(label: &str, ferro: f64, scipy: f64) {
    let ok = if scipy.abs() < 1e-4 {
        // relative tolerance in the deep tail
        let rel = (ferro - scipy).abs() / scipy.abs();
        rel < 1e-6
    } else {
        (ferro - scipy).abs() < 1e-6
    };
    assert!(
        ok,
        "{label}: ferrolearn p={ferro:e} vs scipy p={scipy:e} (|diff|={:e})",
        (ferro - scipy).abs()
    );
}

