//! Re-audit grid for MDS SMACOF (#2405/#2406).
//!
//! Fixed-init parity vs LIVE sklearn 1.5.2 `_smacof_single` (metric=True,
//! init=X0, n_init=1) across many configs. Oracle values produced by
//! `/tmp/mds_oracle.py` (R-CHAR-3 — live sklearn, never copied from ferrolearn).
//! sklearn cite: `sklearn/manifold/_mds.py:104-167`.

use ferrolearn_core::traits::Fit;
use ferrolearn_decomp::{Dissimilarity, MDS};
use ndarray::{Array2, array};

#[allow(
    clippy::too_many_arguments,
    reason = "oracle-config check helper: D/X0/dims + expected stress/embedding/n_iter per fixture"
)]
fn check(
    name: &str,
    d: &Array2<f64>,
    x0: Array2<f64>,
    max_iter: usize,
    eps: f64,
    normalized: bool,
    sk_stress: f64,
    sk_n_iter: usize,
    sk_emb: &Array2<f64>,
) {
    let mut mds = MDS::new(x0.ncols())
        .with_dissimilarity(Dissimilarity::Precomputed)
        .with_init(x0)
        .with_max_iter(max_iter)
        .with_eps(eps);
    if normalized {
        mds = mds.with_normalized_stress(true);
    }
    let fitted = mds.fit(d, &()).expect("fit");
    assert_eq!(fitted.n_iter(), sk_n_iter, "[{name}] n_iter");
    assert!(
        (fitted.stress() - sk_stress).abs() <= 1e-6,
        "[{name}] stress {} vs sklearn {sk_stress}",
        fitted.stress()
    );
    let emb = fitted.embedding();
    assert_eq!(emb.dim(), sk_emb.dim(), "[{name}] emb shape");
    for i in 0..emb.nrows() {
        for k in 0..emb.ncols() {
            assert!(
                (emb[[i, k]] - sk_emb[[i, k]]).abs() <= 1e-6,
                "[{name}] emb[{i},{k}] {} vs sklearn {}",
                emb[[i, k]],
                sk_emb[[i, k]]
            );
        }
    }
}

